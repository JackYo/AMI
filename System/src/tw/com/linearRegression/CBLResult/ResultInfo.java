package tw.com.linearRegression.CBLResult;

public class ResultInfo {
	
	private String date;
	private double predic_elc;
	private double trainScore;
	private double predicScore;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public double getPredic_elc() {
		return predic_elc;
	}
	public void setPredic_elc(double predic_elc) {
		this.predic_elc = predic_elc;
	}
	public double getTrainScore() {
		return trainScore;
	}
	public void setTrainScore(double trainScore) {
		this.trainScore = trainScore;
	}
	public double getPredicScore() {
		return predicScore;
	}
	public void setPredicScore(double predicScore) {
		this.predicScore = predicScore;
	}
	
	

}
